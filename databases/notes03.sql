/* Ограниченная выборка, постраничная выдача */

SELECT * FROM table_name LIMIT 2;           -- Извлекаем две первых записи.
SELECT * FROM table_name LIMIT 4, 2;        -- Пропускаем 4, извлекаем 2.
SELECT * FROM table_name LIMIT 2 OFFSET 4;  -- Пропускаем 4, извлекаем 2.

