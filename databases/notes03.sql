/* UNIXSTAMP */

SELECT
    UNIX_TIMESTAMP('2018-10-10 10:09:23') AS Timestamp,
    FROM_UNIXTIME(1539155363) AS DATETIME;
    -- Timestamp: 1539155363
    -- Datetime: 2018-10-10 10:09:23

-- Возраст:
SELECT name, FLOOR((TO_DAYS(NOW()) - TO_DAYS(birthday_at)) / 365.25) AS age FROM users;
-- Или лучше:
SELECT name, TIMESTAMPDIFF(YEAR, birthday_at, NOW()) AS age FROM users;

