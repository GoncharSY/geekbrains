/* Триггер */

-- Команды, к которым можно привязать триггер:
    -- INSERT
    -- UPDATE
    -- DELETE
-- Триггеры могут выполняться "BEFORE" и "AFTER" каждой из этих команд.
-- Значения в триггерах:
    -- BEFORE NEW.field_name
    -- AFTER OLD.field_name

/*=====================================================================================*/
/* Удалить триггер */
DROP TRIGGER IF EXISTS trigger_name;

/*=====================================================================================*/
/* Создать триггер */
CREATE TRIGGER trigger_name AFTER INSERT ON table_name
FOR EACH ROW
BEBIN
    -- Какие-нибудь действия;
    -- Точно как в процедурах.
END

/*=====================================================================================*/
/* Получить список триггеров */
SHOW TRIGGERS \G;

/*=====================================================================================*/
/* Пример использования */
CREATE TRIGGER trigger_name BEFORE INSERT ON table_name
FOR EACH ROW
BEBIN
    DECLARE var1 INT;
    -- Здесь, если в новой записи поле 'field1' не заполнено.
    -- Тогда оно заполняется значением из переменной 'var1';
    SELECT field1 INTO var1 FROM table_name ORDER BY field1 LIMIT 1;
    SET NEW.field1 = COALESCE(NEW.field1, var1);
END

-- Или немного иначе:
CREATE TRIGGER trigger_name BEFORE UPDATE ON table_name
FOR EACH ROW
BEBIN
    DECLARE var1 INT;
    -- Здесь, если в новой записи поле 'field1' не заполнено.
    -- И старое значение 'field1' тоже было пустым.
    -- Тогда оно заполняется значением из переменной 'var1';
    SELECT field1 INTO var1 FROM table_name ORDER BY field1 LIMIT 1;
    SET NEW.field1 = COALESCE(NEW.field1, OLD.field1, var1);
END

-- А так можно блокировать выполнение запросов:
CREATE TRIGGER trigger_name BEFORE DELETE ON table_name
FOR EACH ROW BEBIN
    DECLARE var_total INT;
    -- Узнаем, сколько сейчас записей в таблице.
    SELECT COUNT(*) INTO var_total FROM table_name;
    -- Не позволим удалить последнюю.
    IF var_total <= 1 THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Удаление отменено';
    END IF;
END
