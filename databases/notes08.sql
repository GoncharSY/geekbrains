/* Создать процедуру (в отдельном файле) */
CREATE PROCEDURE proc_name()
BEGIN
    -- Здесь записываются SQL-команды, как обычно.
    SELECT VERSION();
END

-- Еще можно с меткой:
CREATE PROCEDURE proc_name()
