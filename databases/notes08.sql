/* WHILE..END WHILE */
CREATE PROCEDURE proc_name (IN param1 INT)
BEGIN
    DECLARE i INT DEFAULT 10;
    WHILE i > 0 DO 
        SET i = i - 1;
        -- Еще какие-нибудь действия...
        -- Еще какие-нибудь действия...
    END WHILE;

    -- Другой цикл (с меткой):
    2nd: WHILE i < param1 DO 
        SET i = i + 1;
        -- Еще какие-нибудь действия...
        -- Еще какие-нибудь действия...
        IF i > 100 THEN
            LEAVE 2nd;  -- Досрочный выход.
        END IF;
    END WHILE 2nd;

    -- А здесь мы пропускаем итерации:
    3rd: WHILE i < 1000 DO 
        SET i = i + 1;
        -- Еще какие-нибудь действия...
        IF i < 100 THEN
            ITERATE 3nd;  -- Досрочный переход к следующей итерации.
        END IF;
        -- Еще какие-нибудь действия...
    END WHILE 3rd;
END

-- Чтобы покинуть цикл принудительно, используется команда:
LEAVE cycle_name;

-- Чтобы покинуть итерацию цикла и перейти к следующей:
ITERATE cycle_name;

