/* Курсоры */
-- Должен соблюдаться строгий порядок:
    -- 1. Объявление курсора;
    DECLARE cursor_name CURSOR FOR SELECT * FROM table_name;
    -- 2. Открытие курсора;
    OPEN cursor_name;
    -- 3. Чтение данных курсором (многократно, можно в цикле);
    FETCH cursor_name INTO field1, field2;
    -- 4. Закрытие курсора.
    CLOSE cursor_name;

/*=====================================================================================*/
/* Пример использования курсора */
CREATE PROCEDURE proc_name (IN param1 INT, IN param2 VARCHAR(255))
BEGIN
    DECLARE var1 INT;
    DECLARE var2 TEXT;
    DECLARE is_end INT DEFAULT 0;

    -- Объявляем курсор:
    DECLARE cursor_name CURSOR FOR SELECT field1, field2 FROM table_name;
    -- Объявляем обработчик ошибки:
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET is_end = 1;

    -- Открываем курсор:
    OPEN cursor_name;

    -- Читаем данные:
    1st: LOOP
        FETCH cursor_name INTO var1, var2;
        IF is_end THEN 
            LEAVE 1st;
        END IF;
        -- Делаем, что-нибудь с почитанными данными.
        -- Которые теперь хранятся в переменных.
    END LOOP 1st;

    -- Обязательно не забываем закрыть курсор!!!
    CLOSE cursor_name;
END





