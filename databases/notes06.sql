/*=====================================================================================*/
/* Создание сессионной переменной */

SELECT @var_name := COUNT(*) FROM table_name;

-- Другой вариант.
SET @var_name = NOW() - INTERVAL 7 DAY;

/*=====================================================================================*/
/* Отобразить список системных переменных */

SHOW VARIABLES;
SHOW VARIABLES LIKE 'var_name_%';

/*=====================================================================================*/
/* Изменить глобальную переменную */

SET GLOBAL 'read_buffer_size' = 2097152;

-- Другой вариант.
SET @@global.read_buffer_size = 2097152;

/*=====================================================================================*/
/* Изменить значение сессионной переменной */

SET SESSION 'read_buffer_size' = 2097152;

-- Другой вариант.
SET @@session.read_buffer_size = 2097152;

-- Присвоить значение из глобальной.
SET read_buffer_size = DEFAULT;




