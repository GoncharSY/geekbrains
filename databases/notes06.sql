/* Транзакции */

/*=====================================================================================*/
/* Начать выбор команд, объединненых в транзакцию */
START TRANSACTION;

/*=====================================================================================*/
/* Применить транзакцию */
COMMIT;

-- Все точки сохрения удаляются.

/*=====================================================================================*/
/* Отменить транзакцию */
ROLLBACK;

-- Все точки сохрения удаляются.

/*=====================================================================================*/
/* Создать (или перезаписать) точку сохранения */
SAVEPOINT save_point_name;

/* Откатить изменения к точке сохранения */
ROLLBACK TO SAVEPOINT save_point_name.

/*=====================================================================================*/
/* Отключить автоматическое применение транзакций */

SET AUTOCOMMIT=0;

-- Теперь изменения не будут сохраняться в БД без явной команды COMMIT.
-- Каждая новая команда будет являться частью одной транзакции.

SET AUTOCOMMIT=1;

-- Теперь все работает как и прежде.
-- MySQL сохраняет каждую команду, как отдельную транзакцию.

/*=====================================================================================*/
/* Уровни изоляции транзакций */

READ UNCOMMITTED;   -- Транзакции могут видеть не сохраненные, другие транзакции.
READ COMMITTED;     -- Видит только изменения, зафиксированные другими транзакциями.
REPEATABLE READ;    -- По умолчанию.
SERIALIZABLE;       -- Блокирует каждую строку, которую транзакция читает.

/*=====================================================================================*/
/* Изменить уровень изоляции */

SET TRANSACTION ISOLATION LEVEL READ COMMITTED;




