/*=====================================================================================*/
/*=====================================================================================*/
/* Представления (ракурсы) */

/*=====================================================================================*/
/* Создать представление */

CREATE VIEW view_name1 AS SELECT * FROM table_name;

-- Лучше так:
CREATE OR REPLACE VIEW view_name2 AS SELECT * FROM table_name;

-- Или даже так:
CREATE OR REPLACE VIEW view_name3 AS 
    SELECT * 
    FROM table_name
    WHERE prm1 = 111 AND prm2 = 222;

SHOW TABLES; -- Покажет представление, как таблицу.

-- Алгоритм формирования конечного запроса:
-- MERGE - Представление замещает соотв. части в запросе.
-- TEMPTABLE - Результат помещается во временную таблицу.
-- UNDEFINED - По умолчанию. Автоматическое определение.

-- ВЕРТИКАЛЬНОЕ - Выборка не всех столбцов таблицы.
-- ГОРИЗОНТАЛЬНОЕ - Выборка не всех записей таблицы.

/*=====================================================================================*/
/* Создать обновляемое представление */

CREATE OR REPLACE VIEW view_name AS
    SELECT * 
    FROM table_name
    WHERE prm1 = 111 AND prm2 = 222
    WITH CHECK OPTION;

-- Изменения будут проходить проверку на удовлетворение
-- части 'WHERE' в запросе представления.

/*=====================================================================================*/
/* Отредактировать представление */

ALTER VIEW view_name AS
    SELECT * 
    FROM table_name
    WHERE prm2 > 222
    WITH CHECK OPTION;

-- Или можно использовать: CREATE OR REPLACE VIEW.

/*=====================================================================================*/
/* Удалить представления */

DROP VIEW IF EXISTS view_name1, view_name2, view_name3;




