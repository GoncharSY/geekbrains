/*=====================================================================================*/
/* Временные таблицы и динамические запросы */

/*=====================================================================================*/
/* Создание временной таблицы */

CREATE TEMPORARY TABLE `table_name` (
    `id`    SERIAL,
    `name`  VARCHAR(255),
    ...
);

SHOW TABLES;            -- Не покажет эту табицу.
DESCRIBE `table_name`;  -- Покажет описание таблицы.

/*=====================================================================================*/
/* Создать динамический запрос (для сессии) */

PREPARE dynamic_request FROM 'SELECT VERSION()';

-- Выполнить:
EXECUTE dynamic_request;

/*=====================================================================================*/
/* Динамический запрос с параметром. */

-- Параметр всегда передает значение В ВИДЕ СТРОКИ.
-- Как и остальное тело динамического запроса.
PREPARE dynamic_request 
    FROM 'SELECT id, prm1, prm2 FROM table_name WHERE prm1 = ? AND prm2 = ?';

-- Подготовим переменные:
SET @parameter_1 = 111;     -- Станет строкой.
SET @parameter_2 = 222;     -- Станет строкой.

-- Выполним:
EXECUTE dynamic_request USING @parameter_1, @parameter_2;

/*=====================================================================================*/
/* Удалить запрос */

DROP PREPARE dynamic_request;




