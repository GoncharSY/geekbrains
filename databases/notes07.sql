/* Создать учетную запись */
CREATE USER user_name;
CREATE USER user_name IDENTIFIED WITH sha256_password BY 'password'; -- с паролем.

-- Зайдем под новым пользователем:
mysql -u user_name -p 

-- Узнаем свою учетную запись:
SELECT USER();

-- Узнаем всех пользователей в БД:
SELECT Host, User FROM mysql.user;

-- Переименовать пользователя:
RENAME USER user_name TO new_user_name;

-- Удалить учетную запись:
DROP USER user_name;
-- После этого не забудем закрыть его соединение.
-- Этот пользователь уже не сможет переподключиться к серверу.

-- Имя пользователя:
'user_name'@'localhost' -- пользователь может подключаться только из localhost.
'user_name'@'%' -- пользователь может подключаться из любого хоста.

/*=====================================================================================*/
/* Привилегии */

-- Создать пользователя с полными правами на сервере на все таблицы всех БД.
GRANT ALL ON *.* TO 'user_name'@'host_name' IDENTIFIED WITH sha256_password BY 'password';

-- Отобрать права на все таблицы всех БД:
REVOKE ALL ON *.* FROM 'user_name'@'localhost';

-- Дать привилегии на просмотр, изменение и удаление таблиц:
GRANT SELECT, INSERT, UPDATE, DELETE ON *.* TO user_name;

-- Дать все привилегии, 'ALL' - всегда отдельно:
GRANT ALL ON *.* TO user_name;

-- Дать возможность присваивать привилегии себе другим:
GRANT GRANT OPTION ON *.* TO user_name;

-- Забрать все привилегии, но добавить SELECT:
GRANT USAGE, SELECT ON *.* TO user_name;

-- Дать привилегии только для одной таблицы одной БД:
GRANT ALL ON db_name.table_name TO user_name;

-- Дать читать и изменять только определенные столбцы таблицы:
GRANT SELECT (id, name), UPDATE (name) ON db_name.table_name TO user_name;

/*=====================================================================================*/
/* Просмотр привилегий */
SHOW GRANTS;

-- Еще привилегии:
GRANT ALL ON db_name.* TO 'user_name'@'host_name' IDENTIFIED 
WITH sha256_password BY 'password'
WITH MAX_CONNECTIONS_PER_HOUR 10    -- Не больше 10 подключений к серверу в час.
     MAX_QUERIES_PER_HOUR 1000      -- Не больше 1000 запросов в час.
     MAX_UPDATES_PER_HOUR 200       -- Не больше 200 изменений в час.
     MAX_USER_CONNECTIONS 3;        -- Не больше 3 одновременных подключений.

-- Если значения равны 0 - значит на этот параметр ограничений нет.

