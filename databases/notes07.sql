/* Глобальные переменные (параметры) MySQL */
SHOW VARIABLES LIKE 'date%_format';

-- Узнать уровень изоляции транзакций:
SHOW VARIABLES LIKE 'tx_isolation';

-- Установить значение для сессии:
SET SESSION  tmp_table_size = 16777216;

-- Установить значение для сервера.
-- Сработает после переподключения клиента к серверу.
-- Но как только сервер перезапустится, то значение снова будет взято из '.my.cnf'.
SET GLOBAL  tmp_table_size = 16777216;

