/* Функция benchmark, для измерения времени выполнения */
SET @times = 1000000000; -- сколь раз выполним запрос.
SELECT BENCHMARK(@times, (SELECT COUNT(*) FROM table_name));

-- Вывод лога медленных запросов:
mysqldumpslow mac422rih-slow.log; -- это файл журнала.

